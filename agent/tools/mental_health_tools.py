# 工具 3：CBT 练习推荐
# ──────────────────────────────────────────────────────────────
@tool
def suggest_exercise(emotion_type: str, intensity: Optional[int] = None) -> str:
    """根据用户当前情绪类型和强度，推荐合适的认知行为疗法（CBT）练习或正念活动。包含具体操作步骤。
    适用于：用户说"我该怎么做好受一点"或表达具体情绪困扰。
    参数 emotion_type: 情绪类型，如"焦虑""悲伤""愤怒""压力"
    参数 intensity: 情绪强度 1-10，可选"""
    exercises = {
        "焦虑": {
            "name": "5-4-3-2-1 感官接地法",
            "steps": [
                "看：说出你看到的 5 样东西",
                "摸：感受你触到的 4 种质感",
                "听：辨认你听到的 3 种声音",
                "闻：注意你闻到的 2 种气味",
                "尝：体会你尝到的 1 种味道",
            ],
            "why": "通过调动五感把注意力从焦虑思绪拉回当下现实"
        },
        "悲伤": {
            "name": "行为激活——安排一件小事",
            "steps": [
                "选一件你曾经喜欢但最近没做的事（哪怕很小，如听一首歌）",
                "只计划做 5 分钟，不要要求更久",
                "做完后记录你的情绪变化，哪怕只有一点点好转",
                "每天增加一件小事，不追求完美只追求'做了'",
            ],
            "why": "行为激活是 CBT 核心方法——先改变行为，情绪会跟着变化"
        },
        "压力": {
            "name": "压力清单 + 优先级矩阵",
            "steps": [
                "拿出一张纸，写下所有让你感到压力的事（不筛选，全写）",
                "每条标注：可控 / 不可控",
                "可控的按紧急性-重要性四象限排序",
                "只从'重要且紧急'的第一件事开始",
                "不可控的练习接纳——写出'这件事我无法控制，但我可以控制我的反应'",
            ],
            "why": "压力的核心来源是失控感，区分可控与不可控可以重建掌控感"
        },
        "愤怒": {
            "name": "延迟反应 + 认知重构",
            "steps": [
                "暂停：在心里从 100 倒数到 0，每次减 7（100, 93, 86...）",
                "认知重构：写下'触发我的是什么' → '我的自动反应是什么' → '有没有其他可能的解释'",
                "身体释放：做 20 个深蹲或紧握拳头 10 秒后放松",
                "然后在情绪平复后再决定是否回应",
            ],
            "why": "愤怒的生理唤醒约 20 分钟消退，延迟反应让你不被杏仁核绑架"
        },
    }
    ex = exercises.get(emotion_type, exercises["焦虑"])
    intensity_str = "，强度{}".format(intensity) if intensity else ""
    lines = [
        "推荐练习：{}（适配{}情绪{}）".format(ex["name"], emotion_type, intensity_str),
        "",
        "操作步骤：",
    ]
    for i, step in enumerate(ex["steps"], 1):
        lines.append("  {}. {}".format(i, step))
    lines.append("")
    lines.append("原理：{}".format(ex["why"]))
    return "\n".join(lines)


